#asks the user to enter 3 different integers
int1 = int(input("Enter integer 1: "))
int2 = int(input("Enter integer 2: "))
int3 = int(input("Enter integer 3: "))
#sum of the 3 numbers
total_sum = int1 + int2 + int3
print("Sum of the 3 numbers:", total_sum)
#int1 minus int2
minus = int1 - int2
print("int1 minus int2:", minus)
#int3 multiplied by int1
multiply = int3 * int1
print("int3 multiplied by int1:", multiply)
#the sum of all 3 numbers divided by int3
total_by3 = total_sum / int3
print("The sum of all 3 numbers divided by int3:", total_by3)